package org.example;

/**
 * 例二
 * 加法计算并打印算式
 */
public class Plus {

    public static void main(String[] args) {

        // 声明变量
        int a, b, result;

        // 为变量赋值
        a = 1;
        b = 2;

        // 计算加法结果
        result = a + b;

        // 打印加法算式
        System.out.printf("%d + %d = %d", a, b, result);

    }

}
