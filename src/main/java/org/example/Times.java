package org.example;

/**
 * 例三
 * 乘法计算并打印算式
 */
public class Times {

    public static void main(String[] args) {

        // 声明变量
        int a, b, result;

        // 为变量赋值
        a = 4;
        b = 5;

        // 计算乘法结果
        result = a * b;

        // 打印乘法算式
        System.out.printf("%d x %d = %d", a, b, result);

    }

}
